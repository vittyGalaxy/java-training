package section8;

public class Category {
    private int id;
    private String name;

    // getter
    public int getId(){
        return this.id;
    }

    public String getName(){
        return this.name;
    }

    // setter
    public void setId(int id){
        this.id = id;
    }

    public void setName(String name){
        this.name = name;
    }
}
